class EvenNum1{
    public static void main(String[]args)
    {
        for(int i=1;i<=10;i++)
        {
            if(i%2==0)
            {
                System.out.println(i+" is a Even number");
            }
            else
            {
                System.out.println(i+" is not an even number");
            }
        }
    }
}